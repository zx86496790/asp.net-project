package beans;

/**
 *
 * @author zsen
 */
public class User {
    private String id;
    private int level;
    private String name;
    private String password;

    public User(String id, int level, String name, String password) {
        this.id = id;
        this.level = level;
        this.name = name;
        this.password = password;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
    
}
